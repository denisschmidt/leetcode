/*
Given a binary tree, write a function to get the maximum width of the given tree. 
The width of a tree is the maximum width among all levels. 
The binary tree has the same structure as a full binary tree, but some nodes are null.

The width of one level is defined as the length between the end-nodes 
  (the leftmost and right most non-null nodes in the level, 
  where the null nodes between the end-nodes are also counted into the length calculation.

Example 1:
  Input: 
            1
          /   \
         3     2
        / \     \  
       5   3     9 
  Output: 4
  Explanation: The maximum width existing in the third level with the length 4 (5,3,null,9).

Example 2:
  Input: 
          1
         /  
        3    
       / \       
      5   3     
  Output: 2
  Explanation: The maximum width existing in the third level with the length 2 (5,3).

Example 3:
  Input: 
          1
         / \
        3   2 
       /        
      5      
  Output: 2
  Explanation: The maximum width existing in the second level with the length 2 (3,2).

Example 4:
  Input: 
          1
         / \
        3   2
       /     \  
      5       9 
     /         \
    6           7 
  Output: 8
  Explanation:The maximum width existing in the fourth level with the length 8 (6,null,null,null,null,null,null,7).


Note: Answer will in the range of 32-bit signed integer.

*/

const widthOfBinaryTree = root => {
  if (!root) return 0;
  if (root && !root.left && !root.right) return 1;

  let maxDepth = getDepth(root);
  let queue = [root];
  let depths = [0];
  let nodes = new Map();
  let set = new Set();
  let result = 0;

  while (queue.length) {
    let node = queue.shift();
    let depth = depths.shift();
    let val = node ? node.val : null;

    if (depth > 1 && set.has(depth - 1)) {
      let arr = nodes.get(depth - 1);

      let l = 0;
      let r = arr.length - 1;

      while (arr[l] === null) l++;
      while (arr[r] === null) r--;

      result = Math.max(result, r - l + 1);
      set.add(depth);
      nodes.delete(depth - 1);
    }

    if (!nodes.has(depth)) nodes.set(depth, []);

    nodes.get(depth).push(val);

    if (node) {
      queue.push(node.right);
      queue.push(node.left);

      depths.push(depth + 1);
      depths.push(depth + 1);
    } else if (!node && depth < maxDepth) {
      node = new TreeNode(-1);
      queue.push(node.right);
      queue.push(node.left);

      depths.push(depth + 1);
      depths.push(depth + 1);
    }
  }

  console.log(nodes);

  function getDepth(node) {
    if (!node) return 0;
    return 1 + Math.max(getDepth(node.left), getDepth(node.right));
  }
};

